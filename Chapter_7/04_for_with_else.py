# +++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
#  FOR LOOP WITH ELSE
# +++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++

# for loop also comes with an optional "else" statement which is executed when the loop is exhausted(ended).

for i in range(5):
    print(i)
else:
    print("Loop is ended")